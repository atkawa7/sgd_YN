package com.ztesoft.uboss.bpm.runtime.beans;

public class ProcessVerInfo {
	private String processName;

	private String verState;

	private String processVerId;

	public String getProcessName() {
		return processName;
	}

	public void setProcessName(String processName) {
		this.processName = processName;
	}

	public String getVerState() {
		return verState;
	}

	public void setVerState(String verState) {
		this.verState = verState;
	}

	public String getProcessVerId() {
		return processVerId;
	}

	public void setProcessVerId(String processVerId) {
		this.processVerId = processVerId;
	}

}